class ProductModel {
    constructor(id, name, price, wt, description, category, catid, image){
        this.id = id;
        this.name = name;
        this.price = price;
        this.wt = wt;
        this.description = description;
        this.category = category;
        this.catid = catid;
        this.image = image
    }
}

export default ProductModel