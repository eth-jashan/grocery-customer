class ProductModel {
    constructor(id, name, price, wt, description, category, catid, image, offer, offerPrice){
        this.id = id;
        this.name = name;
        this.price = price;
        this.wt = wt;
        this.description = description;
        this.category = category;
        this.catid = catid;
        this.image = image;
        this.offer = offer 
        this.offerPrice = offerPrice
    }
}

export default ProductModel